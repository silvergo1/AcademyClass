package ch03.unit02;

public class Ex01 {

	public static void main(String[] args) {
		
		int a = 10, b = 5;
		boolean c;
		
		c = a > b;
		System.out.println(c); // 결과 값 = true
		
		System.out.println((a>=b)); // 결과 값 = true
		System.out.println((a<b)); // 결과 값 = false
		System.out.println((a<=b)); // 결과 값 = false
		System.out.println((a==b)); // 결과 값 = false
		System.out.println((a!=b)); // 결과 값 = true

	}

}
